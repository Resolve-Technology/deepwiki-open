# --- save/read roundtrip & newest-wins (functions added in Task 2) ---

def _set_mtime(path, ts):
    os.utime(path, (ts, ts))

def test_save_creates_version_file_and_reads_back(cache_dir):
    req = make_cache_request("claude", "claude-sonnet-4-6")
    assert asyncio.run(save_wiki_cache(req)) is True
    expected = cache_dir / "deepwiki_cache_github_owner_repo_en~claude~claude-sonnet-4-6.json"
    assert expected.exists()

    data = asyncio.run(read_wiki_cache("owner", "repo", "github", "en",
                                       provider="claude", model="claude-sonnet-4-6"))
    assert data is not None
    assert data.provider == "claude"
    assert data.generated_pages["p1"].content == "# Generated by claude/claude-sonnet-4-6"

def test_two_models_do_not_overwrite(cache_dir):
    asyncio.run(save_wiki_cache(make_cache_request("claude", "claude-sonnet-4-6")))
    asyncio.run(save_wiki_cache(make_cache_request("vllm", "Qwen/Qwen3-32B")))
    files = [f for f in os.listdir(cache_dir) if f.endswith(".json")]
    assert len(files) == 2

def test_read_without_version_returns_newest(cache_dir):
    asyncio.run(save_wiki_cache(make_cache_request("claude", "claude-sonnet-4-6")))
    asyncio.run(save_wiki_cache(make_cache_request("vllm", "Qwen/Qwen3-32B")))
    claude_file = cache_dir / "deepwiki_cache_github_owner_repo_en~claude~claude-sonnet-4-6.json"
    vllm_file = cache_dir / "deepwiki_cache_github_owner_repo_en~vllm~Qwen-Qwen3-32B.json"
    _set_mtime(str(claude_file), 1_000_000)
    _set_mtime(str(vllm_file), 2_000_000)

    data = asyncio.run(read_wiki_cache("owner", "repo", "github", "en"))
    assert data.provider == "vllm"

    # Flip mtimes -> the other version is newest now
    _set_mtime(str(claude_file), 3_000_000)
    data = asyncio.run(read_wiki_cache("owner", "repo", "github", "en"))
    assert data.provider == "claude"

def test_read_falls_back_to_legacy_file(cache_dir):
    legacy = cache_dir / "deepwiki_cache_github_owner_repo_en.json"
    req = make_cache_request("google", "gemini-2.0-flash")
    payload = {
        "wiki_structure": req.wiki_structure.model_dump(),
        "generated_pages": {k: v.model_dump() for k, v in req.generated_pages.items()},
        "provider": "google",
        "model": "gemini-2.0-flash",
    }
    legacy.write_text(json.dumps(payload), encoding="utf-8")

    data = asyncio.run(read_wiki_cache("owner", "repo", "github", "en"))
    assert data is not None
    assert data.provider == "google"

def test_read_exact_version_miss_returns_none(cache_dir):
    asyncio.run(save_wiki_cache(make_cache_request("claude", "claude-sonnet-4-6")))
    data = asyncio.run(read_wiki_cache("owner", "repo", "github", "en",
                                       provider="openai", model="gpt-4o"))
    assert data is None

def test_read_skips_corrupt_newest_file(cache_dir):
    asyncio.run(save_wiki_cache(make_cache_request("claude", "claude-sonnet-4-6")))
    corrupt = cache_dir / "deepwiki_cache_github_owner_repo_en~vllm~broken.json"
    corrupt.write_text("{not valid json", encoding="utf-8")
    claude_file = cache_dir / "deepwiki_cache_github_owner_repo_en~claude~claude-sonnet-4-6.json"
    _set_mtime(str(claude_file), 1_000_000)
    _set_mtime(str(corrupt), 2_000_000)

    data = asyncio.run(read_wiki_cache("owner", "repo", "github", "en"))
    assert data is not None
    assert data.provider == "claude"

def test_list_wiki_cache_paths_only_matches_this_repo(cache_dir):
    asyncio.run(save_wiki_cache(make_cache_request("claude", "claude-sonnet-4-6")))
    other = cache_dir / "deepwiki_cache_github_owner_repo2_en~claude~m.json"
    other.write_text("{}", encoding="utf-8")
    paths = list_wiki_cache_paths("owner", "repo", "github", "en")
    assert len(paths) == 1
    assert "repo2" not in paths[0]


# --- DELETE endpoint (Task 3) ---

def test_delete_specific_version_only(cache_dir):
    asyncio.run(save_wiki_cache(make_cache_request("claude", "claude-sonnet-4-6")))
    asyncio.run(save_wiki_cache(make_cache_request("vllm", "Qwen/Qwen3-32B")))

    result = asyncio.run(api_module.delete_wiki_cache(
        owner="owner", repo="repo", repo_type="github", language="en",
        provider="claude", model="claude-sonnet-4-6", authorization_code=None))
    assert "deleted" in result

    remaining = [f for f in os.listdir(cache_dir) if f.endswith(".json")]
    assert remaining == ["deepwiki_cache_github_owner_repo_en~vllm~Qwen-Qwen3-32B.json"]

def test_delete_without_version_removes_all(cache_dir):
    asyncio.run(save_wiki_cache(make_cache_request("claude", "claude-sonnet-4-6")))
    asyncio.run(save_wiki_cache(make_cache_request("vllm", "Qwen/Qwen3-32B")))
    (cache_dir / "deepwiki_cache_github_owner_repo_en.json").write_text("{}", encoding="utf-8")

    asyncio.run(api_module.delete_wiki_cache(
        owner="owner", repo="repo", repo_type="github", language="en",
        provider=None, model=None, authorization_code=None))
    assert [f for f in os.listdir(cache_dir) if f.endswith(".json")] == []

