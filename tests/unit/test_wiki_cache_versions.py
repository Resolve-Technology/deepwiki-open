@pytest.fixture
def cache_dir(tmp_path, monkeypatch):
    """Point the module-level cache dir at a temp dir."""
    monkeypatch.setattr(api_module, "WIKI_CACHE_DIR", str(tmp_path))
    return tmp_path


def make_cache_request(provider="claude", model="claude-sonnet-4-6"):
    page = WikiPage(
        id="p1",
        title="Page 1",
        content=f"# Generated by {provider}/{model}",
        filePaths=["a.py"],
        importance="high",
        relatedPages=[],
    )
    structure = WikiStructureModel(
        id="wiki", title="Wiki", description="d", pages=[page]
    )
    return WikiCacheRequest(
        repo=RepoInfo(owner="owner", repo="repo", type="github"),
        language="en",
        wiki_structure=structure,
        generated_pages={"p1": page},
        provider=provider,
        model=model,
    )


# --- sanitize_version_segment ---

def test_sanitize_passthrough():
    assert sanitize_version_segment("claude-sonnet-4-6") == "claude-sonnet-4-6"

def test_sanitize_replaces_slash():
    assert sanitize_version_segment("Qwen/Qwen3-32B") == "Qwen-Qwen3-32B"

def test_sanitize_is_idempotent():
    once = sanitize_version_segment("Qwen/Qwen3-32B")
    assert sanitize_version_segment(once) == once

def test_sanitize_empty_and_none():
    assert sanitize_version_segment("") == ""
    assert sanitize_version_segment(None) == ""
    assert sanitize_version_segment("///") == ""


